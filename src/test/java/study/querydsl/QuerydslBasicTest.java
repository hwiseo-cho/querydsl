    @Test
    public void dynamicQuery_booleanBuilder() {
        String uesrnameParam = "member1";
        Integer ageParam = 10;

        List<Member> result = searchMember1(uesrnameParam, ageParam);
    }

    private List<Member> searchMember1(String uesrnameParam, Integer ageParam) {

        BooleanBuilder builder = new BooleanBuilder();

        if (uesrnameParam != null) {
            builder.and(member.username.eq(uesrnameParam));
        }

        if (ageParam != null) {
            builder.and(member.age.eq(ageParam));
        }

        return queryFactory
                .select(member)
                .from(member)
                .where(builder)
                .fetch();
    }

    @Test
    public void dynamicQuery_whereParam() {
        String uesrnameParam = "member1";
        Integer ageParam = 10;

        List<Member> result = searchMember2(uesrnameParam, ageParam);
    }

    private List<Member> searchMember2(String usernameParam, Integer ageParam) {
        return queryFactory
                .select(member)
                .from(member)
                .where(usernameEq(usernameParam), ageEq(ageParam))
                .fetch();
    }

    private Predicate usernameEq(String usernameParam) {
        return usernameParam != null ? member.username.eq(usernameParam) : null;
    }

    private Predicate ageEq(Integer ageParam) {
        return ageParam != null ? member.age.eq(ageParam) : null;
    }
    
